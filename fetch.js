const data = { text: "Hello, :earth_africa:" };

fetch("https://makers-emojify.herokuapp.com", {
  method: "POST", // or 'PUT'
  headers: {
    "Content-Type": "application/json",
  },
  body: JSON.stringify(data),
})
  .then((response) => response.json())
  .then((data) => {
    console.log("Success:", data);
  });
